def _layout(**kw):
    base = dict(
        template="plotly_dark",
        plot_bgcolor=PLOT_BG,
        paper_bgcolor=PLOT_BG,
        font=dict(color=FONT_CLR, family="DM Sans, sans-serif", size=12),
        xaxis=dict(gridcolor=GRID_CLR, zeroline=False, linecolor="rgba(255,255,255,0.08)"),
        yaxis=dict(gridcolor=GRID_CLR, zeroline=False, linecolor="rgba(255,255,255,0.08)"),
        margin=dict(l=60, r=30, t=55, b=50),
        hoverlabel=dict(
            bgcolor="#161b27", font_size=12,
            font_family="DM Sans, sans-serif",
            bordercolor="rgba(255,255,255,0.12)",
        ),
        legend=dict(bgcolor="rgba(0,0,0,0)", font_size=11),
    )
    base.update(kw)
    return base


