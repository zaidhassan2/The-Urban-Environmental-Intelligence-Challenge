def page_overview():
    st.header("Dataset Overview")

    summary = load_station_summary()
    stats   = load_stats()

    if summary.is_empty():
        _missing("Overview")
        return

    # ── metrics (values pulled directly from processed data) ──
    n_stations  = len(summary)
    n_countries = summary["country"].n_unique() if "country" in summary.columns else 0
    n_regions   = summary["region"].n_unique()
    avg_pm25    = summary["mean_pm25"].mean()
    p99_val     = float(stats["p99"][0]) if not stats.is_empty() else None

    c1, c2, c3, c4, c5 = st.columns(5)
    c1.metric("Stations", f"{n_stations}")
    c2.metric("Countries", f"{n_countries}")
    c3.metric("Regions", f"{n_regions}")
    c4.metric("Avg Mean PM2.5", f"{avg_pm25:.1f} µg/m³")
    c5.metric("Station 99th Percentile", f"{p99_val:.1f} µg/m³" if p99_val else "—")

    st.divider()

    col_a, col_b = st.columns(2)

    with col_a:
        st.subheader("Mean PM2.5 by Region")
        region_means = (
            summary.group_by("region")
                   .agg(pl.col("mean_pm25").mean().alias("avg"))
                   .sort("avg", descending=True)
        )
        regions = region_means["region"].to_list()
        vals    = region_means["avg"].to_list()
        colours = [C_RED if v > PM25_THRESHOLD else C_BLUE for v in vals]

        fig = go.Figure(go.Bar(
            y=regions, x=vals, orientation="h",
            marker_color=colours,
            text=[f"{v:.1f} µg/m³" for v in vals],
            textposition="outside",
            textfont=dict(size=10, color=FONT_CLR),
            hovertemplate="<b>%{y}</b><br>Mean PM2.5: %{x:.2f} µg/m³<extra></extra>",
        ))
        fig.add_vline(
            x=PM25_THRESHOLD, line_dash="dash", line_color=C_RED, line_width=1.2,
            annotation_text=f"WHO threshold  {PM25_THRESHOLD} µg/m³",
            annotation_position="top",
            annotation_font=dict(color=C_RED, size=10),
        )
        fig.update_layout(**_layout(
            xaxis_title="Annual mean PM2.5 (µg/m³)",
            height=340,
        ))
        st.plotly_chart(fig, use_container_width=True)

    with col_b:
        st.subheader("Zone Type Comparison")
        zone_df = (
            summary.group_by("zone_type")
                   .agg([
                       pl.col("mean_pm25").mean().alias("avg"),
                       pl.len().alias("n"),
                   ])
                   .sort("zone_type")
        )
        zones  = zone_df["zone_type"].to_list()
        zvals  = zone_df["avg"].to_list()
        zcols  = [ZONE_COLOURS.get(z, "#888") for z in zones]
        counts = zone_df["n"].to_list()

        fig2 = go.Figure(go.Bar(
            x=zones, y=zvals,
            marker_color=zcols, width=0.4,
            text=[f"{v:.1f} µg/m³<br>n = {c}" for v, c in zip(zvals, counts)],
            textposition="outside",
            textfont=dict(size=10, color=FONT_CLR),
            hovertemplate="<b>%{x}</b><br>Mean PM2.5: %{y:.2f} µg/m³<extra></extra>",
        ))
        fig2.add_hline(
            y=PM25_THRESHOLD, line_dash="dash", line_color=C_RED, line_width=1.2,
            annotation_text=f"WHO threshold  {PM25_THRESHOLD} µg/m³",
            annotation_position="top left",
            annotation_font=dict(color=C_RED, size=10),
        )
        fig2.update_layout(**_layout(
            yaxis_title="Annual mean PM2.5 (µg/m³)",
            height=340,
        ))
        st.plotly_chart(fig2, use_container_width=True)

    st.subheader("Station Summary")
    display_cols = [c for c in
                    ["station_id", "zone_type", "region", "country",
                     "mean_pm25", "p95_pm25", "n_readings"]
                    if c in summary.columns]
    st.dataframe(
        summary.select(display_cols)
               .sort("mean_pm25", descending=True)
               .rename({
                   "station_id": "Station",
                   "zone_type":  "Zone",
                   "region":     "Region",
                   "country":    "Country",
                   "mean_pm25":  "Mean PM2.5",
                   "p95_pm25":   "P95 PM2.5",
                   "n_readings": "Readings",
               })
               .to_pandas(),
        use_container_width=True,
    )


