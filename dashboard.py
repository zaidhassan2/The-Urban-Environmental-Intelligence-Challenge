def page_task1():
    st.header("Task 1 – Dimensionality Reduction")

    scores_df   = load_pca_scores()
    loadings_df = load_pca_loadings()

    if scores_df.is_empty() or loadings_df.is_empty():
        _missing("Task 1")
        return

    pc1_var = float(scores_df["pc1_variance"][0])
    pc2_var = float(scores_df["pc2_variance"][0])
    n_sta   = len(scores_df)
    combined = pc1_var + pc2_var

    c1, c2, c3, c4 = st.columns(4)
    c1.metric("Stations Analysed", f"{n_sta}")
    c2.metric("PC1 Variance", f"{pc1_var*100:.1f}%")
    c3.metric("PC2 Variance", f"{pc2_var*100:.1f}%")
    c4.metric("Combined Variance", f"{combined*100:.1f}%")

    st.divider()
    col1, col2 = st.columns(2)

    with col1:
        st.subheader("Principal Component Scatter")
        fig = go.Figure()
        for zone, colour in ZONE_COLOURS.items():
            sub = scores_df.filter(pl.col("zone_type") == zone)
            if len(sub):
                fig.add_trace(go.Scatter(
                    x=sub["pc1"].to_numpy(),
                    y=sub["pc2"].to_numpy(),
                    mode="markers",
                    name=zone,
                    marker=dict(color=colour, size=10, opacity=0.85,
                                line=dict(width=0)),
                    hovertemplate=(
                        f"<b>{zone}</b><br>"
                        "PC1: %{x:.3f}<br>"
                        "PC2: %{y:.3f}<extra></extra>"
                    ),
                ))
        fig.add_hline(y=0, line_color="rgba(255,255,255,0.12)", line_width=0.8)
        fig.add_vline(x=0, line_color="rgba(255,255,255,0.12)", line_width=0.8)
        fig.update_layout(**_layout(
            xaxis_title=f"PC1  ({pc1_var*100:.1f}% variance explained)",
            yaxis_title=f"PC2  ({pc2_var*100:.1f}% variance explained)",
            height=460,
        ))
        st.plotly_chart(fig, use_container_width=True)

    with col2:
        st.subheader("Variable Loadings")
        labels   = loadings_df["display_name"].to_list()
        pc1_vals = loadings_df["pc1_loading"].to_list()
        pc2_vals = loadings_df["pc2_loading"].to_list()

        fig2 = make_subplots(
            rows=1, cols=2, shared_yaxes=True,
            subplot_titles=[
                f"PC1  ({pc1_var*100:.1f}%)",
                f"PC2  ({pc2_var*100:.1f}%)",
            ],
            horizontal_spacing=0.08,
        )
        for ci, (vals, col_name) in enumerate([(pc1_vals, "PC1"), (pc2_vals, "PC2")], 1):
            bar_cols = [C_RED if v < 0 else C_BLUE for v in vals]
            fig2.add_trace(go.Bar(
                y=labels, x=vals, orientation="h",
                marker_color=bar_cols, showlegend=False,
                hovertemplate=f"<b>%{{y}}</b><br>{col_name}: %{{x:.3f}}<extra></extra>",
            ), row=1, col=ci)
            fig2.add_vline(x=0, line_color="rgba(255,255,255,0.2)",
                           line_width=0.8, row=1, col=ci)

        fig2.update_xaxes(range=[-1.05, 1.05], title_text="Loading coefficient",
                          gridcolor=GRID_CLR)
        fig2.update_yaxes(gridcolor=GRID_CLR)
        for ann in fig2["layout"]["annotations"]:
            ann["font"] = dict(size=11, color=FONT_CLR)
        fig2.update_layout(**_layout(height=460))
        st.plotly_chart(fig2, use_container_width=True)

    with st.expander("Analysis"):
        pc1_load = dict(zip(loadings_df["parameter"].to_list(),
                            loadings_df["pc1_loading"].to_list()))
        pc2_load = dict(zip(loadings_df["parameter"].to_list(),
                            loadings_df["pc2_loading"].to_list()))
        st.markdown(f"""
**PC1 – Pollution Axis ({pc1_var*100:.1f}% variance)**
PM10 ({pc1_load.get('pm10', 0):+.3f}), PM2.5 ({pc1_load.get('pm25', 0):+.3f}), NO2 ({pc1_load.get('no2', 0):+.3f}) load positively on PC1. Industrial stations score highest, confirming this axis captures combustion and particulate burden.

**PC2 – Meteorological Axis ({pc2_var*100:.1f}% variance)**
Temperature ({pc2_load.get('temperature', 0):+.3f}) and Humidity ({pc2_load.get('humidity', 0):+.3f}) dominate with opposing signs. This axis separates stations by climate regime — hot/dry versus cool/humid — independent of pollution source.

**Method justification:** Air quality variables are correlated, so PCA finds the directions of maximum variance. {combined*100:.1f}% of total variance captured in two dimensions confirms the data has low intrinsic dimensionality. Loadings map directly back to physical sensor quantities, making results interpretable without specialist knowledge.
        """)

    with st.expander("Processed Data"):
        st.markdown("**PCA Scores**")
        st.dataframe(scores_df.to_pandas(), use_container_width=True)
        st.markdown("**Loadings**")
        st.dataframe(loadings_df.to_pandas(), use_container_width=True)


