    p_hlth   = float(s["p_health"])
    p_extr   = float(s["p_extreme"])
    station  = s["station_id"]
    zone     = s["zone_type"]
    n_reads  = int(s["n"])
    hrs_yr   = p_hlth * 8760

    c1, c2, c3, c4, c5 = st.columns(5)
