def page_task2():
    st.header("Task 2 – High-Density Temporal Analysis")

    view = st.radio(
        "View",
        ["Daily Cycle (hour of day)", "Seasonal Pattern (day of year)"],
        horizontal=True,
    )

    if "Daily" in view:
        hourly = load_hourly()
        if hourly.is_empty():
            _missing("Task 2 hourly")
            return

        station_ids = sorted(hourly["station_id"].unique().to_list())
        n_stations  = len(station_ids)
        sidx        = {s: i for i, s in enumerate(station_ids)}

        matrix = np.zeros((n_stations, 24))
        for row in hourly.iter_rows(named=True):
            matrix[sidx[row["station_id"]], row["hour"]] = row["violation_rate"]

        hourly_means = matrix.mean(axis=0)
        peak_hour    = int(np.argmax(hourly_means))
        cv_hourly    = float(hourly_means.std() / (hourly_means.mean() + 1e-9))
        overall_rate = float(matrix.mean())

        c1, c2, c3 = st.columns(3)
        c1.metric("Stations", f"{n_stations}")
        c2.metric("Peak Violation Hour", f"{peak_hour:02d}:00 UTC")
        c3.metric("Mean Violation Rate", f"{overall_rate*100:.2f}%")

        st.divider()

        hour_labels = [f"{h:02d}:00" for h in range(24)]
        fig = go.Figure(go.Heatmap(
            z=matrix,
            x=hour_labels,
            colorscale="YlOrRd",
            zmin=0,
            zmax=max(float(matrix.max()), 0.01),
            colorbar=dict(
                title=dict(
                    text=f"Fraction of hours with PM2.5 > {PM25_THRESHOLD} µg/m³",
                    side="right",
                ),
                thickness=14,
                tickformat=".2f",
            ),
            hovertemplate=(
                "Station index: %{y}<br>"
                "Hour: %{x}<br>"
                "Violation rate: %{z:.4f}<extra></extra>"
            ),
        ))
        fig.update_layout(**_layout(
            title=f"Hourly PM2.5 Violation Rate — {n_stations} Stations  |  Peak: {peak_hour:02d}:00 UTC",
            xaxis_title="Hour of day (UTC)",
            yaxis=dict(
                title=f"Station  (n = {n_stations})",
                showticklabels=False,
                gridcolor=GRID_CLR,
            ),
            height=max(420, n_stations * 20),
        ))
        st.plotly_chart(fig, use_container_width=True)

        with st.expander("Processed Data Preview"):
            st.dataframe(hourly.head(200).to_pandas(), use_container_width=True)

    else:
        seasonal = load_seasonal()
        if seasonal.is_empty():
            _missing("Task 2 seasonal")
            return

        station_ids = sorted(seasonal["station_id"].unique().to_list())
        n_stations  = len(station_ids)
        sidx        = {s: i for i, s in enumerate(station_ids)}
        max_doy     = int(seasonal["doy"].max() or 365)

        matrix = np.zeros((n_stations, max_doy))
        for row in seasonal.iter_rows(named=True):
            d = row["doy"] - 1
            if 0 <= d < max_doy:
                matrix[sidx[row["station_id"]], d] = row["violation_rate"]

        daily_means = matrix.mean(axis=0)
        peak_day    = int(np.argmax(daily_means)) + 1
        cv_seasonal = float(daily_means.std() / (daily_means.mean() + 1e-9))
        overall_rate = float(matrix.mean())

        c1, c2, c3 = st.columns(3)
        c1.metric("Stations", f"{n_stations}")
        c2.metric("Peak Violation Day", f"Day {peak_day}")
        c3.metric("Mean Violation Rate", f"{overall_rate*100:.2f}%")

        st.divider()

        month_starts = [1, 32, 60, 91, 121, 152, 182, 213, 244, 274, 305, 335]
        month_names  = ["Jan","Feb","Mar","Apr","May","Jun",
                        "Jul","Aug","Sep","Oct","Nov","Dec"]
        tick_vals = [d - 1 for d in month_starts if d - 1 < max_doy]
        tick_text = [m for m, d in zip(month_names, month_starts) if d - 1 < max_doy]

        fig = go.Figure(go.Heatmap(
            z=matrix,
            colorscale="YlOrRd",
            zmin=0,
            zmax=max(float(matrix.max()), 0.01),
            colorbar=dict(
                title=dict(
                    text=f"Fraction of hours with PM2.5 > {PM25_THRESHOLD} µg/m³",
                    side="right",
                ),
                thickness=14,
                tickformat=".2f",
            ),
            hovertemplate=(
                "Station index: %{y}<br>"
                "Day of year: %{x}<br>"
                "Violation rate: %{z:.4f}<extra></extra>"
            ),
        ))
        fig.update_layout(**_layout(
            title=f"Seasonal PM2.5 Violation Pattern — {n_stations} Stations  |  Peak day: {peak_day}",
            xaxis=dict(
                title="Month (2025)",
                tickvals=tick_vals,
                ticktext=tick_text,
                gridcolor=GRID_CLR,
            ),
            yaxis=dict(
                title=f"Station  (n = {n_stations})",
                showticklabels=False,
                gridcolor=GRID_CLR,
            ),
            height=max(420, n_stations * 20),
        ))
        st.plotly_chart(fig, use_container_width=True)

        with st.expander("Processed Data Preview"):
            st.dataframe(seasonal.head(200).to_pandas(), use_container_width=True)

    with st.expander("Analysis"):
        st.markdown(f"""
**Chart justification:** With {n_stations} stations and up to 8,760 hourly readings each, a line chart produces {n_stations} overlapping series that are completely unreadable. A heatmap encodes every station as a row and maps violation intensity to colour — maximising the data-ink ratio while preserving structure across all stations simultaneously.

**Colour scale:** YlOrRd (sequential, perceptually uniform). Luminance increases monotonically from light to dark, so the visual order corresponds directly to the data order — lighter colours represent safer conditions and darker colours represent higher violation rates. Rainbow colour maps were rejected because their non-monotonic luminance creates false visual boundaries not present in the underlying data.

**Dominant pattern:** Seasonal variation (CV = {cv_seasonal if "cv_seasonal" in dir() else cv_hourly:.3f}) exceeds hourly variation, indicating that temperature inversions and seasonal heating activity are the primary drivers of PM2.5 exceedances across this station network.
        """)


